a = int(input("Masukan Nilai Adit : "))

if a>=90 and a<=100:
    print("Maka Grade Nilai Adit adalah A")
elif a>=80 and a<90:
    print("Maka Grade Nilai Adit adalah B+")
elif a>=70 and a<80:
    print("Maka Grade Nilai Adit adalah B")
elif a>=60 and a<70:
    print("Maka Grade Nilai Adit adalah C+")   
elif a>=50 and a<60:
    print("Maka Grade Nilai Adit adalah C")   
elif a>=40 and a<50:
    print("Maka Grade Nilai Adit adalah D")  
elif a>=30 and a<40:
    print("Maka Grade Nilai Adit adalah E")   
else:
    print("Adit adalah mahasiswa DO")